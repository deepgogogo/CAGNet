def calBoxMat(boxes,OH, OW):
    N=boxes.shape[0]
    sqrt2 = math.sqrt(2)
    xij = abs((boxes[st[N]][:, 2] + boxes[st[N]][:, 0]) - (boxes[ed[N]][:, 2] + boxes[ed[N]][:, 0])) / (2 * OW)
    yij = abs((boxes[st[N]][:, 3] + boxes[st[N]][:, 1]) - (boxes[ed[N]][:, 3] + boxes[ed[N]][:, 1])) / (2 * OH)
    dij = torch.sqrt(xij * xij + yij * yij) / sqrt2
    wij = torch.min(abs(boxes[st[N]][:, 2] - boxes[st[N]][:, 0]),
                        abs(boxes[ed[N]][:, 2] - boxes[ed[N]][:, 0])) / torch.max(
            abs(boxes[st[N]][:, 2] - boxes[st[N]][:, 0]), abs(boxes[ed[N]][:, 2] - boxes[ed[N]][:, 0]))
    hij = torch.min(abs(boxes[st[N]][:, 3] - boxes[st[N]][:, 1]),
                        abs(boxes[ed[N]][:, 3] - boxes[ed[N]][:, 1])) / torch.max(
            abs(boxes[st[N]][:, 3] - boxes[st[N]][:, 1]), abs(boxes[ed[N]][:, 3] - boxes[ed[N]][:, 1]))
    high_level_info = torch.stack((
            dij, torch.pow(dij, 2), torch.exp(-dij),
            xij, torch.pow(xij, 2), torch.exp(-xij),
            yij, torch.pow(yij, 2), torch.exp(-yij),
            wij, torch.pow(wij, 2), torch.exp(-wij),
            hij, torch.pow(hij, 2), torch.exp(-hij)
        ), dim=1)
    high_level_info=torch.squeeze(high_level_info)
    # print("high_level_info.shape={}".format(high_level_info.shape))
    return  high_level_info

